package week2;

public class PositiveNegative {

	public static void main(String[] args) {
		
		int number = 10;

		if(number > 0) {
			System.out.println(number + " Is Positive Number");
		}
		else {
			System.out.println(number + " Is Negative");
		}
	}

}

//Console   10 Is Positive Number

